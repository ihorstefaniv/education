let second = 1;
console.log("This is " + second + "second");

let minute = second * 60;
console.log("In the minute: " + minute + "seconds");

let hour = minute * 60;
console.log("In the hour: " + hour + "seconds");

let day = hour * 24;
console.log("In the day: " + day + "seconds");

let mounth = day * 30;
console.log("In the mounth: " + mounth + "seconds");

let year = day * 365;
console.log("In the year: " + year + "seconds");
